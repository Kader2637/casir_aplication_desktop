    private void loadBarangKeTabel() {
        tableModel.setRowCount(0);

        for (Barang barang : BarangDAO.getAllBarang()) {
            Object[] row = {
                barang.getNama(),
                barang.getDeskripsi(),
                barang.getStok(),
                barang.getKode(),
                barang.getHarga()
            };
            tableModel.addRow(row);
        }
    }


