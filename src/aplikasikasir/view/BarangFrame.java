    private void btnSimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan1ActionPerformed
        String nama = txtNama.getText();
        String kode = txtKodeBarang.getText();
        String stokStr = txtStok.getText();
        String harga = txtHarga.getText(); 
        String deskripsi = txtDeskripsi.getText();

        try {
            int stok = Integer.parseInt(stokStr);

            Barang barang = new Barang(nama, kode, stok, harga, deskripsi);

            boolean sukses;
            if (isUpdateMode) {
                sukses = BarangDAO.updateBarang(barang, kodeLama);
            } else {
                sukses = BarangDAO.insertBarang(barang);
            }

            if (sukses) {
                loadBarangKeTabel();
                resetForm();
                JOptionPane.showMessageDialog(this, isUpdateMode ? "Data berhasil diupdate!" : "Data berhasil disimpan!");
            } else {
                JOptionPane.showMessageDialog(this, isUpdateMode ? "Gagal update data." : "Gagal menyimpan data.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stok harus berupa angka!");
        }
    }//GEN-LAST:event_btnSimpan1ActionPerformed

    private void tableBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBarangMouseClicked
        int selectedRowIndex = tableBarang.getSelectedRow();

        if (selectedRowIndex >= 0 && evt.getClickCount() == 2) {
            // Ambil data dari baris yang diklik
            String nama = tableModel.getValueAt(selectedRowIndex, 0).toString();
            String deskripsi = tableModel.getValueAt(selectedRowIndex, 1).toString();
            String stok = tableModel.getValueAt(selectedRowIndex, 2).toString();
            String kode = tableModel.getValueAt(selectedRowIndex, 3).toString();
            String harga = tableModel.getValueAt(selectedRowIndex, 4).toString();

            String[] options = {"Edit", "Hapus", "Batal"};
            int choice = JOptionPane.showOptionDialog(
                    this,
                    "Apa yang ingin kamu lakukan?",
                    "Aksi Data",
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.INFORMATION_MESSAGE,
                    null,
                    options,
                    options[0]
            );

            if (choice == 0) {
                txtNama.setText(nama);
                txtDeskripsi.setText(deskripsi);
                txtStok.setText(stok);
                txtHarga.setText(harga);
                txtKodeBarang.setText(kode);

                isUpdateMode = true;
                kodeLama = kode;

            } else if (choice == 1) {
                boolean sukses = BarangDAO.deleteBarang(kode);
                if (sukses) {
                    loadBarangKeTabel();
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                    resetForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data dari database.");
                }
            }
        }
    }//GEN-LAST:event_tableBarangMouseClicked

